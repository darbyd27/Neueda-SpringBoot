package Test;

class Animal
{//Class Start
	
	//Variables
	private String name;
	
	public Animal() {
		super();
	}

	//Constructor
	public Animal(String name)
	{
		super();
		this.name = name;
	}

	//Getters and Setters
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}//Class End